    private void NumberListener(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_NumberListener
       String digit = evt.getActionCommand();
       if (number) {
         txtField.setText(digit);
   	 number = false;
      } else {
    	txtField.setText(txtField.getText() + digit);
      }
    }//GEN-LAST:event_NumberListener

    private void OperatorListener(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OperatorListener
    if (number&&(!"=".equals(equalOp))) {
            action();
            txtField.setText("0");
      } else {
    	number = true;
   	 String displayText = txtField.getText();
   	 switch(equalOp) {
   	   case "=":
   	 	calculatorOp.setTotal(displayText);
   	   break;
   	   case "+":
   	 	calculatorOp.add(displayText);
   	   break;
   	   case "-":
   	 	calculatorOp.subtract(displayText);
           break;
   	   case "*":
   	 	calculatorOp.multiply(displayText);
   	   break;
   	   case "/":
   	 	calculatorOp.divide(displayText);
   	   break;
   	 }
   	 if("C".equals(evt.getActionCommand()))
             action();
         else{
             txtField.setText("" + calculatorOp.getTotalString());
             equalOp = evt.getActionCommand();
         }

      }
    }//GEN-LAST:event_OperatorListener

