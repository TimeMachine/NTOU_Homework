  private void action() {
    number = true;
    txtField.setText("0");
    equalOp = "=";
    calculatorOp.setTotal("0");
  }

    class CalculatorOp {
      private double total;
      public CalculatorOp() {
   	 total = 0;
      }
      
      public String getTotalString() {
          if(total%1==0){
             return String.valueOf((int)total);
          }
   	 return String.valueOf(total);
      }
      
      public void setTotal(String value) {
   	 total = Double.parseDouble(value);
      }
      
      public void add(String value) {
         BigDecimal b1 = new BigDecimal(Double.toString(total));
         BigDecimal b2 = new BigDecimal(value);
         total = b1.add(b2).doubleValue();
      }
      
      public void subtract(String value) {
          BigDecimal b1 = new BigDecimal(Double.toString(total));
         BigDecimal b2 = new BigDecimal(value);
         total = b1.subtract(b2).doubleValue();
      }
      
      public void multiply(String value) {
          BigDecimal b1 = new BigDecimal(Double.toString(total));
         BigDecimal b2 = new BigDecimal(value);
         total = b1.multiply(b2).doubleValue();
      }
      
      public void divide(String value) {
   	  try{
              BigDecimal b1 = new BigDecimal(Double.toString(total));
              BigDecimal b2 = new BigDecimal(value);
              total = b1.divide(b2).doubleValue();
          }
          catch (Exception  e) {
              action();
              txtField.setText("ERROR");
        }
      }
    }
    
